
public class TupleWXYZ {
	private int x;
	private int y;
	private byte[] w;
	private byte[] z;
	
	public TupleWXYZ()
	{
	}
	public void setX(int x)
	{
		this.x = x;
	}
	public void setY(int y)
	{
		this.y = y;
	}
	public void setW(byte[] w)
	{
		this.w = w;
	}
	public void setZ(byte[] z)
	{
		this.z = z;
	}
	public int getX()
	{
		return x;
	}
	public int getY()
	{
		return y;
	}
	public byte[] getW()
	{
		return w;
	}
	public byte[] getZ()
	{
		return z;
	}
}
