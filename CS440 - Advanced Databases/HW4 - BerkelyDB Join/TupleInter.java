
public class TupleInter {
	private int x;
	private int y;
	private byte[] w;
	
	public TupleInter()
	{
	}
	public void setX(int x)
	{
		this.x = x;
	}
	public void setY(int y)
	{
		this.y = y;
	}
	public void setW(byte[] w)
	{
		this.w = w;
	}
	public int getX()
	{
		return x;
	}
	public int getY()
	{
		return y;
	}
	public byte[] getW()
	{
		return w;
	}
}
